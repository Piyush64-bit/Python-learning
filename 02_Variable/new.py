# Variable Basics in Python

# Integers
age = 21
print("Age:", age)

# Float (Decimal numbers)
height = 5.9
print("Height in feet:", height)

# String (Text)
name = "Piyush"
print("Name:", name)

# Boolean (True/False)
is_student = True
print("Are you a student?", is_student)

# Multiple assignments
x, y, z = 10, 20, 30
print("X:", x, "Y:", y, "Z:", z)

# Type checking
print("Data type of name:", type(name))
print("Data type of height:", type(height))

# Update a variable
age = age + 1  # or age += 1
print("Updated Age:", age)

# Type casting
year = "2025"
year_number = int(year)
print("Next year will be:", year_number + 1)

#  Bonus: User input
fav_color = input("What's your fav color? ")
print("Your favorite color is:", fav_color)
